void rotatef5(unsigned char *in) {
        unsigned char a,c;
        a = in[0];
        for(c=0;c<3;c++) 
                in[c] = in[c + 1];
        in[3] = a;
        return;
}

/* Calculate the rcon_f2 used in key expansion */
unsigned char rconf5(unsigned char in) {
        unsigned char c=1;
        if(in == 0)  
                return 0; 
        while(in != 1) {
		unsigned char b;
		b = c & 0x80;
		c <<= 1;
		if(b == 0x80) {
			c ^= 0x1b;
		}
                in--;
        }
        return c;
}

/* This is the core key expansion, which, given a 4-byte value,
 * does some scrambling */
void schedule_coref5(unsigned char *in, unsigned char i) {
        char a;
        /* rotate_f2 the input 8 bits to the left */
        rotatef5(in);
        /* Apply Rijndael's s-box on all 4 bytes */
        for(a = 0; a < 4; a++) 
                in[a] = sboxf5[in[a]];
        /* On just the first byte, add 2^i to the byte */
        in[0] ^= rconf5(i);
}



